package _01_Operators;

public class _02_RelationalOperator {
    public static void main(String[] args) {
        int var_1 = 10;
        int var_2 = 5;

        System.out.println(var_1 > var_2);
        System.out.println(var_1 < var_2);
        System.out.println(var_1 >= var_2);
        System.out.println(var_1 <= var_2);
        System.out.println(var_1 == var_2);
        System.out.println(var_1 != var_2);
    }
}
