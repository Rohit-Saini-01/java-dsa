package _01_Operators;

public class _03_LogicalOperators {
    public static void main(String[] args) {
        boolean var_1 = true;
        boolean var_2 = false;

        System.out.println(var_1&&var_2);
        System.out.println(var_1||var_2);
        System.out.println(!var_2);
        System.out.println(!var_1);

    }
}
