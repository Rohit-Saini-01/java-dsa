package _01_Operators;

public class _04_AssignmentOperator {
    public static void main(String[] args) {
        int var_1 = 10;

        var_1 += 5;
        System.out.println(var_1);

        var_1 -= 5;
        System.out.println(var_1);

        var_1 *= 5;
        System.out.println(var_1);

        var_1 /= 5;
        System.out.println(var_1);
    }
}
